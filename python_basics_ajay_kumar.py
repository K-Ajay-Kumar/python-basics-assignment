# Task 1: Variables and Data Types
name = "John Doe"        # string
age = 20                 # integer
height = 1.75            # float (in meters)
is_student = True        # boolean

# Print all values in a single statement
print(name, age, height, is_student)

# Task 2: String Formatting
print(f"Name: {name} | Age: {age} | Height: {height} | Student: {is_student}")

# Task 3: Arithmetic Operations

# Age calculations
age_in_months = age * 12
age_in_days = age * 365
remainder = age % 7
age_squared = age ** 2

# Print results
print("Age in months:", age_in_months)
print("Age in days:", age_in_days)
print("Remainder when age is divided by 7:", remainder)
print("Age squared:", age_squared)